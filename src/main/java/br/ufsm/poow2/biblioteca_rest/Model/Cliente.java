package br.ufsm.poow2.biblioteca_rest.Model;

public class Cliente {

    public int id;
    public String nome;

    public Cliente() {}

    public Cliente(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
